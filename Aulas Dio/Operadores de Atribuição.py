#Atribuição Simples
saldo = 500
print(saldo)

#Atribuição com Adição
saldo = 500
saldo += 200
print(saldo)

#Atribuição com Subtração
saldo = 500
saldo -= 200
print(saldo)

#Atribuição com Multiplicação
saldo = 500
saldo *= 2
print(saldo)

#Atribuição com Divisão
saldo = 500
saldo /=5
print(saldo)

saldo = 500
saldo //=5
print(saldo)

#Atribuição com Módulo
saldo = 500
saldo %= 480
print(saldo)

#Atribuição com Expoenciação
saldo = 50
saldo **= 2
print(saldo)